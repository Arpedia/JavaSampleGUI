    private void readFile(String filepath){        
        File path = new File(filepath);
        String line;
        try{
            BufferedReader reader = new BufferedReader(new InputStreamReader(new FileInputStream(path),"UTF-8"));
            line= reader.readLine();
            this.students = new ArrayList<Seiseki>();
            while((line = reader.readLine()) != null){
                System.out.println(line);
                this.importSeiseki(line);
            }
        } catch(IOException e){
            System.out.println("IO Error");
        }
        
    }
    
    private void importSeiseki(String line){
        String data[] = line.split("\t",0);
        Seiseki stu = new Seiseki(data[0], data[1]);
        ArrayList<Integer> score = new ArrayList<Integer>();
        score.add(Integer.parseInt(data[2]));
        score.add(Integer.parseInt(data[3]));
        score.add(Integer.parseInt(data[4]));
        score.add(Integer.parseInt(data[5]));
        score.add(Integer.parseInt(data[6]));
        stu.setScore(score);
        System.out.println(stu);
        this.students.add(stu);
    }


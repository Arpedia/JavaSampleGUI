    private void NameInputButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NameInputButtonActionPerformed
        // TODO add your handling code here:
        String name = NameField.getText();
        for(Seiseki student: students){
            if (student.getName().equals(name)){
                ArrayList<Integer> score = student.getScore();
                JapaneseScore.setText(String.valueOf(score.get(0)));
                if(score.get(0) < 60){
                    JapanesePanel.setBackground(Color.RED);
                }
                MathScore.setText(String.valueOf(score.get(1)));
                if(score.get(1) < 60){
                    MathPanel.setBackground(Color.RED);
                }
                EnglishScore.setText(String.valueOf(score.get(2)));
                if(score.get(2) < 60){
                    EnglishPanel.setBackground(Color.RED);
                }
                PhisicsScore.setText(String.valueOf(score.get(3)));
                if(score.get(3) < 60){
                    PhisicsPanel.setBackground(Color.RED);
                }
                ComuScore.setText(String.valueOf(score.get(4)));
                if(score.get(3) < 60){
                    ComuPanel.setBackground(Color.RED);
                }
            }else{
                ErrorLabel.setText("");
            }
        }
    }//GEN-LAST:event_NameInputButtonActionPerformed
